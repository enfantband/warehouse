package com.samsbeauty.old.model;

import java.util.List;

public class LocationItemTransferInput {

	private String fromLocationBarcode;
	private String toLocationBarcode;
	private String toBoxBarcode;
	private String gid;
	private List<WarehouseItemBean> items;
	
	public String getFromLocationBarcode() {
		return fromLocationBarcode;
	}
	public void setFromLocationBarcode(String locationBarcode) {
		this.fromLocationBarcode = locationBarcode;
	}
	public String getToLocationBarcode() {
		return toLocationBarcode;
	}
	public void setToLocationBarcode(String locationBarcode) {
		this.toLocationBarcode = locationBarcode;
	}
	public List<WarehouseItemBean> getItems() {
		return items;
	}
	public void setItems(List<WarehouseItemBean> items) {
		this.items = items;
	}
	public String getGid() {
		return gid;
	}
	public void setGid(String gid) {
		this.gid = gid;
	}
	public String getToBoxBarcode() {
		return toBoxBarcode;
	}
	public void setToBoxBarcode(String toBoxBarcode) {
		this.toBoxBarcode = toBoxBarcode;
	}
}
