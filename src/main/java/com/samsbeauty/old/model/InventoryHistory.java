package com.samsbeauty.old.model;

public class InventoryHistory {
	private String actionId;
	private String barcode;
	private String orderNo;
	private String status;
	private String statusName;
	private String quantity;
	private String sumQuantity;
	private String riEiGid;
	private String riEiName;
	private String riDate;
	private String reason;
	private String reasonName;

	public String getActionId() {
		return actionId;
	}
	public void setActionId(String actionId) {
		this.actionId = actionId;
	}

	public String getBarcode() {
		return barcode;
	}
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}

	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

	public String getStatusName() {
		return statusName;
	}
	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getSumQuantity() {
		return sumQuantity;
	}
	public void setSumQuantity(String sumQuantity) {
		this.sumQuantity = sumQuantity;
	}
	public String getRiEiGid() {
		return riEiGid;
	}
	public void setRiEiGid(String riEiGid) {
		this.riEiGid = riEiGid;
	}

	public String getRiDate() {
		return riDate;
	}
	public void setRiDate(String riDate) {
		this.riDate = riDate;
	}
	
	public String getRiEiName() {
		return riEiName;
	}
	public void setRiEiName(String riEiName) {
		this.riEiName = riEiName;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getReasonName() {
		return reasonName;
	}
	public void setReasonName(String reasonName) {
		this.reasonName = reasonName;
	}
}