package com.samsbeauty.warehouse.picking.report.model;

import java.util.List;

public class PickingJobTimelineReportForChartJS<T> {
	private List<String> labels;
	private List<ChartJsColumn<T>> datasets;
	
	public List<String> getLabels() {
		return labels;
	}
	public void setLabels(List<String> labels) {
		this.labels = labels;
	}
	public List<ChartJsColumn<T>> getDatasets() {
		return datasets;
	}
	public void setDatasets(List<ChartJsColumn<T>> datasets) {
		this.datasets = datasets;
	}
}
